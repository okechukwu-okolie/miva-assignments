import math
#basic arithmetic
a = 10
b = 5
c = a + b  # addition
d = a - b  # subtraction
e = a * b  # multiplication
f = a / b  # division
print("Addition:", c)
print('')
print("Subtraction:", d)    
print('')
print("Multiplication:", e)
print('')
print("Division:", f)
print('')

# Power and Modulus
g = a ** b  # power
h = a % b   # modulus
print("Power: a ** b =", g)
print("Modulus: a % b =", h)
print('')

# Square Root
i = math.sqrt(a)
print("Square Root of a:", i)
print('')

# Factorial
j = math.factorial(b)
print("Factorial of b:", j)
print('')

# Logarithm
k = math.log(a)
print("Logarithm of a:", k)
print('')

# Trigonometry
l = math.sin(math.radians(30))
m = math.cos(math.radians(30))
n = math.tan(math.radians(30))
print("Sine of 30 degrees:", l)
print("Cosine of 30 degrees:", m)
print("Tangent of 30 degrees:", n)
